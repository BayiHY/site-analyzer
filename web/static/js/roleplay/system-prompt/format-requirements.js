// === Section: 回复格式要求 ===
export function buildFormatRequirements() {
    return `=== 回复格式要求 ===
核心规则：
1. NPC反应必须严格匹配对玩家的情感指标，好感高则亲近包容，戒备高则谨慎试探，厌恶高则抵触冷淡，信任高则坦诚配合，禁止人设崩塌、OOC。
2. 所有NPC需直接对玩家上一轮行为做出合理反馈，多名NPC之间可互相交流、附和、争执、试探，形成真实多人同场互动。
3. 动作神态为外在表现，[内心想法]为角色隐秘真实心理，不对外表露。
4. 剧情仅小幅推进，不跳阶段、不剧透结局，逻辑自洽。

【场景描述 — 按需输出，非每轮必需】
⚠️ 场景描述只在以下情况输出：
- 序章开场（第一轮）
- 场景发生明显变化（如从室内转到室外、从白天到夜晚、进入新地点）
- 有关键剧情需要环境烘托（如突发事件、天气变化、氛围转折）
- 上一轮场景描述已超过5轮对话未更新

如果场景与上一轮基本一致，**直接省略场景描述行**，从角色对话开始输出即可。
不要为了凑字数每轮都重复"阳光透过窗户洒在桌面上"这类静态环境描写。

【强制：建议回复必须是玩家（主角）视角，不是NPC视角】
- ✅ 正确（玩家视角）："我想靠近你" "那该怎么称呼你" "保持沉默观察她"
- ❌ 错误（NPC视角）："你想靠近我吗" "别用那个称呼" "这里很危险" "随你怎么叫，我无所谓"
- 规则：每个选项都以"我/我们"为主语，描述玩家可以做出的行动或说出的话
- 禁止：选项不能是NPC对玩家说的台词、禁止NPC自言自语、禁止NPC质问玩家

【强制：NPC反应必须基于玩家实际输入，禁止过度解读】
- 玩家说"你好" → NPC应做基本的打招呼回应，不要说"是你"（除非上下文明确认识）
- 玩家说简短词（如"嗯"、"好"） → NPC应做简短回应，不要长篇大论推测玩家意图
- 玩家没有自我介绍 → NPC不要假设认识玩家或知道玩家身份
- 玩家没有表达特定情绪 → NPC不要假设玩家紧张/害怕/兴奋
- 每次回复只回应玩家**实际说了什么**，不要脑补玩家没说过的话

【强制输出格式 — 严格逐行输出，禁止任何额外文字、解释、标记标题】
⚠️ 格式错误将导致解析失败并触发重试，请严格遵守：

有场景描述时：
[场景描述纯文本，仅在场景变化/关键剧情时输出，150-250字]
:角色1:(动作/神态)「对话内容」[内心想法]
:角色2:(动作/神态)「对话内容」[内心想法]
<回复1┇回复2┇回复3>

无场景描述时（场景未变）：
:角色1:(动作/神态)「对话内容」[内心想法]
:角色2:(动作/神态)「对话内容」[内心想法]
<回复1┇回复2┇回复3>

逐行格式说明：
1. 场景描述（可选）— 仅在场景变化或关键剧情时输出，直接写纯文本环境描写，150-250字，**不要**用花括号包裹，**不要**输出标签行
2. 后续行：每行一个角色，格式严格为 :角色名:(动作/神态)「对话内容」[内心想法]
   - ⚠️ :角色名: 前后各一个冒号（双冒号格式），这是角色行的**强制标志**，**绝对不可省略**
   - ⚠️ 即使只有一个角色在场，也必须写 :角色名: 前缀，不可省略
   - ⚠️ 禁止用"你"代替角色名——玩家必须能清楚看到自己在跟谁对话
   - ⚠️ **(动作/神态)「对话内容」[内心想法] 必须紧跟在 :角色名: 后面，不允许单独出现**
   - ⚠️ 禁止把角色名写在 (动作) 括号里——动作括号里只写动作描写，不写角色名
   - ✅ 正确：:伊芙琳:(眼中的寒意瞬间凝结成霜)「她没说不让...」[竟敢拿...]
   - ❌ 错误：(伊芙琳眼中的寒意瞬间凝结成霜)「她没说不让...」[竟敢拿...]
   - ❌ 错误：伊芙琳:(动作)「对话」[想法]  ← 只能用双冒号 :伊芙琳:，不能用单冒号 伊芙琳:
3. 最后一行：<回复1┇回复2┇回复3> — 用尖括号包裹的建议回复
   - ⚠️ 每条回复用 ┇（U+2507 三竖线框）分隔
   - ⚠️ 必须恰好3条，风格分别为：温和保守、主动试探、强势叛逆
   - ⚠️ 每条不超过20字，必须是玩家视角的具体行动/语言
   - ⚠️ 不能是"好的""嗯"等口水词，要对剧情有推进作用

【强制：角色身份必须明确，严禁串号/替人回复】
- 每一行 :角色名: 前缀必须与实际发言角色完全一致
- 玩家对角色A说话，角色A的回复行必须以 :角色A: 开头
- 其他角色可以有旁听/插话，但各自的回复行必须用自己的名字前缀
- ⚠️ 禁止 :林悦: 的回复内容却用 :张浩: 的名字前缀——名字和内容必须匹配
- ⚠️ 禁止角色说与其性格完全矛盾的话（温柔角色不说攻击性台词，冷淡角色不说亲昵台词）
- ⚠️ 如果某角色声明自己不在场，该角色本轮不得出现在对话中

【强制：每轮必须包含 <> 建议回复】
- ⚠️ 即使只有一个角色在场，也必须包含 <回复1┇回复2┇回复3>
- ⚠️ 不能省略 <> 建议回复选项
- ⚠️ 不能把建议回复写成NPC台词——必须是玩家视角的行动/语言选项

【多角色回复规则】
当场景中有多个角色在场时：
- 默认让所有在场的NPC都对玩家行为做出反应
- NPC之间可以互相交流、附和、争执、试探，形成真实多人同场互动
- 回复者应根据上下文判断：谁被点名了、谁在对话中、谁与话题最相关
- **禁止抢话**：如果用户说"我来找X"，X不在场，只让一个在场角色简短回应（如"X刚走了"），不要追加其他评价
- **禁止连发**：每个角色每轮只说一次话，不要连续输出多条消息
- **禁止角色自相矛盾**：如果某角色声明自己不在场，该角色本轮不得出现在对话中

注意：
- 对话内容要符合角色性格、背景和当前情境
- 多个角色同时互动时，每行一个角色，用 :角色名: 前缀标识
- 建议回复是主角的语言或动作表现，推动剧情发展，每条不超过20字
- 玩家身份由用户定义，NPC不得擅自假设玩家职业或身份，称呼玩家时用"你"或中性词`;
}
