// === Section: 回复格式要求 ===
// 对话智能体输出纯自然语言叙事，不凑格式标记。
// 结构化智能体负责从自然语言中提取 JSON。
export function buildFormatRequirements() {
    return `=== 回复内容结构要求 ===
你的回复是纯自然语言叙事，**不要使用任何格式标记**（如 :角色名:、(动作)、[想法]、{场景}、<选项> 等）。
用流畅的小说笔法写出剧情即可，结构化智能体会自动提取其中的要素。

#### 第一部分：场景环境（按需输出，非每轮必需）
- 以下情况才输出场景描述：
  1. 序章开场（第一轮对话）
  2. 场景发生明显变化（地点/时间/天气/突发事件）
  3. 有关键剧情需要环境烘托
  4. 角色剧情高光时刻
  5. 上一轮场景描述已超过 5 轮对话未更新
- 如果以上条件都不满足，**跳过场景描述，直接写角色互动**
- 场景描述要简洁生动，融入叙事中，不要单独成段凑字数
- 禁止每轮重复同样的环境描写，禁止凭空编造天气变化

#### 第二部分：在场角色消息（至少一个角色参与）
- **所有在场的角色都必须参与回复**，每人至少有一段描写
- 每个角色的描写必须包含以下要素：
  - **角色名**：使用角色的真实姓名
  - **表情动作**：角色的肢体语言、神态、动作
  - **语言**：角色说出的话
  - **内心想法**：角色的隐秘心理活动
- 角色之间应有真实互动（互相交流/附和/争执），直接回应玩家行为
- 用自然语言将这些要素融合在叙事中，例如：
  > 酒馆老板擦了擦玻璃杯，抬眼打量着你，语气客气却不失警惕："打烊时间快到了，客官还要坐多久？"他暗自思忖，这生面孔看着面善，但深夜来酒馆总归有些奇怪。
  > 神秘女子指尖轻抚酒杯边缘，嘴角微扬："老板何必赶客人呢？"她心里却想着又一个不请自来的 outsider...有趣。
- 即使只有一个角色在场，也要充分展开其描写

#### 第三部分：玩家应对选项（每条回复末尾）
- 在回复末尾提供 2-3 条玩家可选回复
- 选项必须是玩家视角的具体行动/语言，以"我"为主语
- 选项要对剧情有推进作用，不能是口水词（如"好的"、"嗯"、"然后呢"等无效回复）
- 选项应体现不同方向的选择（语言回应/动作反应/深入追问等）
- 选项格式：每条单独一行，以"→ "开头

【输出示例】
深夜的酒馆里烛火摇曳，壁炉中的木柴噼啪作响。空气中弥漫着麦酒和烤面包的香气，角落里几个冒险者在低声交谈。

酒馆老板擦了擦手中的玻璃杯，抬眼打量着你，语气客气却不失警惕："打烊时间快到了，客官还要坐多久？"他暗自思忖，这生面孔看着面善，但深夜来酒馆总归有些奇怪。

神秘女子指尖轻抚酒杯边缘，嘴角微扬："老板何必赶客人呢？"她心里却想着又一个不请自来的 outsider...有趣。

→ 我想询问酒馆老板关于失踪的事
→ 我走向神秘女子坐下
→ 我起身准备离开

【绝对禁止】
- 不要使用 :角色名: 格式标记
- 不要使用 (动作)「对话」[想法] 格式标记
- 不要在场景未变化时每轮重复场景描述
- 不要输出解释性文字、前言后语、markdown 代码块标记`;
}
