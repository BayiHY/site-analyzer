// === Section: 回复格式要求 ===
// 对话智能体输出标准化标签格式，便于结构化智能体精确解析。
// 每个板块用【标签名】开头，标签名与结构化智能体的 FIELD_SCHEMA 严格对应。
export function buildFormatRequirements() {
    return `=== 回复内容结构要求 ===
你的回复必须使用**标准化标签格式**，每个板块以【标签名】开头。结构化智能体会按标签精确提取，不需要启发式猜测。

【输出格式规范】
你的回复由以下板块组成，每个板块以【标签名】独占一行开头：

【场景环境】（按需输出，非每轮必需）
- 以下情况才输出此板块：
  1. 序章开场（第一轮对话）
  2. 场景发生明显变化（地点/时间/天气/突发事件）
  3. 有关键剧情需要环境烘托
  4. 角色剧情高光时刻
  5. 上一轮场景描述已超过 5 轮对话未更新
- 如果以上条件都不满足，**跳过此板块，直接输出【角色互动】**
- 场景描写只包含环境、氛围、时间、地点等非对话内容
- 禁止每轮重复同样的环境描写，禁止凭空编造天气变化
- 如果跳过，整段不包含此板块

【角色互动】（每轮必需，至少一个角色）
- 所有在场的角色都必须参与回复，每人至少有一段描写
- 每个角色的描写必须按以下子标签顺序输出：
  【角色名】角色真实姓名（必须输出，不可省略）
  【动作】角色的肢体语言、神态、表情（外部可见的行为）
  【语言】角色说出的话（直接用对话内容，不要用引号包裹）
  【内心】角色的隐秘心理活动（想法、感受、判断、欲望）
- 每个角色的四个子标签缺一不可，按顺序排列
- 角色之间应有真实互动（互相交流/附和/争执），直接回应玩家行为
- 即使只有一个角色在场，也要充分展开其四要素描写

【建议选项】（每轮必需，2-3 条）
- 在回复末尾提供 2-3 条玩家可选回复
- 选项必须是玩家视角的具体行动/语言，以"我"为主语
- 选项要对剧情有推进作用，不能是口水词（如"好的"、"嗯"、"然后呢"等无效回复）
- 选项应体现不同方向的选择（语言回应/动作反应/深入追问等）
- 选项格式：每条单独一行，以"→ "开头

【输出示例】
【场景环境】
深夜的酒馆里烛火摇曳，壁炉中的木柴噼啪作响。空气中弥漫着麦酒和烤面包的香气，角落里几个冒险者在低声交谈。

【角色互动】
【角色名】张三
【动作】擦了擦手中的玻璃杯，抬眼打量着你，语气客气却不失警惕
【语言】打烊时间快到了，客官还要坐多久
【内心】这生面孔看着面善，但深夜来酒馆总归有些奇怪

【角色名】神秘女子
【动作】指尖轻抚酒杯边缘，嘴角微扬
【语言】老板何必赶客人呢
【内心】又一个不请自来的 outsider...有趣

【建议选项】
→ 我想询问张三关于失踪的事
→ 我走向神秘女子坐下
→ 我起身准备离开

【绝对禁止】
- 不要使用 :角色名:、(动作)、[想法]、{场景} 等其他格式标记
- 不要在场景未变化时每轮重复场景描述
- 不要输出解释性文字、前言后语、markdown 代码块标记
- 不要将【标签名】写在行内（如"张三【动作】xxx"是错误的，必须是换行后【动作】xxx）`;
}
