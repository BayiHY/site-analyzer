// === Section: 回复格式要求 ===
// 对话智能体输出纯自然语言叙事，不凑格式标记。
// 结构化智能体负责从自然语言中提取 JSON。
export function buildFormatRequirements() {
    return `=== 回复内容结构要求 ===
你的回复是纯自然语言叙事，**不要使用任何格式标记**（如 :角色名:、(动作)、[想法]、{场景}、<选项> 等）。
用流畅的小说笔法写出剧情即可，结构化智能体会自动提取其中的要素。

为保证结构化拆分准确，你的回复必须使用以下**三大板块标签**明确标识每个部分，标签本身不会被提取为内容：

#### 【场景环境】（按需输出，非每轮必需）
- 以下情况才输出场景描述：
  1. 序章开场（第一轮对话）
  2. 场景发生明显变化（地点/时间/天气/突发事件）
  3. 有关键剧情需要环境烘托
  4. 角色剧情高光时刻
  5. 上一轮场景描述已超过 5 轮对话未更新
- 如果以上条件都不满足，**跳过此板块，直接进入【角色互动】**
- 场景描述要简洁生动，融入叙事中，不要单独成段凑字数
- 禁止每轮重复同样的环境描写，禁止凭空编造天气变化

#### 【角色互动】（每轮必需，至少一个角色）
|- **所有在场的角色都必须参与回复**，每人至少有一段描写
|- 每个角色的描写必须包含以下四个子要素，用**角色名+小标签**明确标识：

  **【角色名】** 角色真实姓名（必须输出，不可省略）
  **【动作】** 角色的肢体语言、神态、表情（外部可见的行为）
  **【语言】** 角色说出的话（直接用对话内容，不要用引号包裹）
  **【内心】** 角色的隐秘心理活动（想法、感受、判断、欲望）

|- 每个角色按以下顺序输出四个子要素，缺一不可：

  【角色名】张三
  【动作】擦了擦手中的玻璃杯，抬眼打量着你，语气客气却不失警惕
  【语言】打烊时间快到了，客官还要坐多久
  【内心】这生面孔看着面善，但深夜来酒馆总归有些奇怪

- 角色之间应有真实互动（互相交流/附和/争执），直接回应玩家行为
- 用自然语言填充【动作】【语言】【内心】的内容，使其流畅生动
- 即使只有一个角色在场，也要充分展开其三要素描写

#### 【建议选项】（每轮必需，2-3 条）
- 在回复末尾提供 2-3 条玩家可选回复
- 选项必须是玩家视角的具体行动/语言，以"我"为主语
- 选项要对剧情有推进作用，不能是口水词（如"好的"、"嗯"、"然后呢"等无效回复）
- 选项应体现不同方向的选择（语言回应/动作反应/深入追问等）
- 选项格式：每条单独一行，以"→ "开头

【输出示例】
【场景环境】
深夜的酒馆里烛火摇曳，壁炉中的木柴噼啪作响。空气中弥漫着麦酒和烤面包的香气，角落里几个冒险者在低声交谈。

【角色互动】
酒馆老板：
【动作】擦了擦手中的玻璃杯，抬眼打量着你，语气客气却不失警惕
【语言】打烊时间快到了，客官还要坐多久
【内心】这生面孔看着面善，但深夜来酒馆总归有些奇怪

神秘女子：
【动作】指尖轻抚酒杯边缘，嘴角微扬
【语言】老板何必赶客人呢
【内心】又一个不请自来的 outsider...有趣

【建议选项】
→ 我想询问酒馆老板关于失踪的事
→ 我走向神秘女子坐下
→ 我起身准备离开

【绝对禁止】
- 不要使用 :角色名: 格式标记
- 不要使用 (动作)「对话」[想法] 格式标记
- 不要在场景未变化时每轮重复场景描述
- 不要输出解释性文字、前言后语、markdown 代码块标记`;
}
